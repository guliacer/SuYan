const normalizedCategoryMap = new Map<string, string>(
  [
    ...photographyCategoryLabels.map((category): [string, string] => [
      normalizePhotographyCategoryKey(category),
      category,
    ]),
    ...photographyCategoryAliases.map(([alias, category]): [string, string] => [
      normalizePhotographyCategoryKey(alias),
      category,
    ]),
  ],
);

export function resolvePhotographyCategory(value: string): string | null {
  const normalizedValue = normalizePhotographyCategoryKey(value);

  if (!normalizedValue) {
    return null;
  }

  return normalizedCategoryMap.get(normalizedValue) ?? null;
}

export function normalizePhotographyCategorySuggestions(values: readonly string[]): string[] {
  const suggestions: string[] = [];

  for (const value of values) {
    const category = resolvePhotographyCategory(value);

    if (!category || suggestions.includes(category)) {
      continue;
    }

    suggestions.push(category);
  }

  return suggestions;
}

export function getHomePhotographyCategorySet(label: string): ReadonlySet<string> | null {
  const item = homePhotographyCategoryNavItems.find((navItem) => navItem.label === label);

  return item ? new Set(item.categories) : null;
}

export function buildPhotographyCategoryPromptList(): string {
  return photographyCategoryGroups
    .map((group) => {
      const categories = group.categories
        .map(([label, description], index) => `${index + 1}. ${label}：${description}`)
        .join("\n");

      return `【${group.group}】\n${categories}`;
    })
    .join("\n\n");
}

function normalizePhotographyCategoryKey(value: string): string {
  return value
    .trim()
    .toLowerCase()
    .replace(/\s+/g, "")
    .replace(/[，,。；;：:、（）()【】[\]{}《》<>「」"'“”‘’\-_/|]/g, "");
}
